package org.example;

public class Conditionals {
    public static void main(String[] args){
        int a = 40;
        if(a>30){
            System.out.println("true at if");
        } else if (a<70) {
            System.out.println("true at else-if");
        } else {
            System.out.println("true at else");
        }
    }
}
